//===========================================================================================================
// 初期設定
//===========================================================================================================
void CCreateBlock::Init()
{
	int MAX_V = 10;
	int MAX_H = 10;

	srand((unsigned int)time(NULL)); // 現在時刻の情報で初期化
	
	for (int i = 0; i < MAX_V; i++)
	{
		for (int o = 0; o < MAX_H; o++)
		{
			int r = rand() % MAX_V + 1;
			D3DXVECTOR3 pos = { 0.0f + (i * 20),0.0f,0.0f };
			D3DXVECTOR3 scale = { 1.0f,1.0f,1.0f };
			CBlockEnemy::Create(pos, scale);
		}
	}
}

