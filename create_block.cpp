//===========================================================================================================
// 初期設定
//===========================================================================================================
void CCreateBlock::Init()
{
	int MAX = 10;

	srand((unsigned int)time(NULL)); // 現在時刻の情報で初期化
	
	for (int i = 0; i < MAX; i++)
	{
		int r = rand() % MAX + 1;
		D3DXVECTOR3 pos = { 100.0f + (r * 20),0.0f,0.0f };
		D3DXVECTOR3 scale = { 1.0f,1.0f,1.0f };
		CBlockEnemy::Create(pos, scale);
	}
}

